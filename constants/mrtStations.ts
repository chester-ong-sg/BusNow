/** Euclidean distance < 0.012 degrees ≈ ~1.2 km radius for train stations */
export function nearbyMrtStations(
  lat: number,
  lng: number,
  radiusDeg = 0.012
): MrtStation[] {
  return MRT_STATIONS.filter((s) => {
    const d = Math.sqrt(
      Math.pow(s.latitude - lat, 2) + Math.pow(s.longitude - lng, 2)
    );
    return d < radiusDeg;
  }).sort((a, b) => {
    const da = Math.sqrt(Math.pow(a.latitude - lat, 2) + Math.pow(a.longitude - lng, 2));
    const db = Math.sqrt(Math.pow(b.latitude - lat, 2) + Math.pow(b.longitude - lng, 2));
    return da - db;
  });
}
